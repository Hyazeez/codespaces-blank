function remainder(fn,sn){
    return fn % sn;
}
console.log(remainder(10,3));
1

function add(fn,sn){
    return fn + sn;
}
console.log(add(10,3));
13

function subtract(fn,sn){
    return fn - sn;
}
console.log(subtract(10,3));
7

function multiplication(fn,sn){
    return fn * sn;
}
console.log(multiplication(10,3));
30

function division(fn,sn){
    return fn / sn;
}
console.log(division(10,3));
3.3333333333333335
